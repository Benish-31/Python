print("Operations in Maths exercise")
sign = input("Enter the sign Bitch: ")
sa = int(input("Enter your first wish number: "))
sb = int(input("Enter your second wish number: "))

if sign == "+":
    print("Igiteranyo cya", sa, "na", sb, "ni", sa+sb)

elif sign == "-":
    print("Dukuyemo cya", sa, "na", sb, "ni", sa-sb)
elif sign == "*":
    print("Dukubye cya", sa, "na", sb, "ni", sa*sb)
elif sign == "/":
    print("Tugabanyije cya", sa, "na", sb, "ni", sa/sb)
else:
    print("Invalid Bitch!!!!!!!!!!!!!!!!!!!")
    
